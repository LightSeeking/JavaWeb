
// 常用方法
var arr = [1,2,3,4]
console.log(arr)

var add = arr.push('追加的数据')
console.log(arr)
console.log(add)

var del = arr.pop();
console.log(arr)
console.log(del)

var start = arr.unshift('添加的数据')
console.log(arr)
console.log(start)

var res = arr.shift()
console.log(arr)
console.log(res)

var back = arr.reverse()
console.log(arr)
console.log(back)

